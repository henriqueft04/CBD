package pt.ua.deti.ies;

class Restaurant{
    public String id;
    public String name;
    public String location;
    public String gastronomy;

    public Restaurant(String id, String name, String location, String gastronomy) {
        this.id = id;
        this.name = name;
        this.location = location;
        this.gastronomy = gastronomy;
    }

}